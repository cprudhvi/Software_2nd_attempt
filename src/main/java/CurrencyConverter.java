public class CurrencyConverter {
    public CurrencyConverter(){

    }
    public int dollar_to_rs(int dollar){
        int rs1 = dollar * 70;
        return rs1;
    }
    public int sek_to_rs(int sek){
        int rs2 = sek * 9;
        return rs2;
    }
    public int euro_to_rs(int euro){
        int rs3 = euro * 100;
        return rs3;
    }
}

